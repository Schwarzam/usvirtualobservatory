package dalserver;

/** All possible range element types. */
public enum RangeType {
    /** A single value. */
    ONEVAL,

    /** Any value greater than or equal to this value. */
    LOVAL,

    /** Any value less than or equal to this value. */
    HIVAL,

    /** Any value in the specfied closed range (inclusive). */
    CLOSED,

    /** Any value. */
    ANY
}
